export const getImage = (team: Team) => {
  const city = team.city.toLowerCase().replace(" ", "-");
  const name = team.name.toLowerCase();
  const imageName = `${city}-${name}`;
  if (team.urlImage) return team.urlImage;
  return `https://loodibee.com/wp-content/uploads/nfl-${imageName}-team-logo-2-350x350.png`;
};

