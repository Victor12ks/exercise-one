export const getDetailsTeam = (team?: Team) => {
  const items: DescriptionsProps["items"] = [
    {
      label: "Name",
      span: { xl: 2, xxl: 2 },
      children: `${team?.city} ${team?.name}`,
    },
    {
      label: "City",
      span: { xl: 2, xxl: 2 },
      children: team?.cityStadium,
    },
    {
      label: "Abrev",
      span: { xl: 2, xxl: 2 },
      children: team?.abr,
    },
    {
      label: "Established",
      span: { xl: 2, xxl: 2 },
      children: team?.establishedYear,
    },
    {
      label: "Stadium",
      span: { xs: 1, sm: 2, md: 3, lg: 3, xl: 2, xxl: 2 },
      children: team?.stadium,
    },
    {
      label: "Capacity",
      span: { xs: 1, sm: 2, md: 3, lg: 3, xl: 2, xxl: 2 },
      children: team?.capacityStadium,
    },
    {
      label: "Super Bowls",
      span: { xs: 1 },
      children: team?.superBowlChamps.join(", "),
    },
  ];
  return items;
};
